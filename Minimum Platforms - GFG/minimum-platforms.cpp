// Program to find minimum number of platforms
// required on a railway station
#include <bits/stdc++.h>
using namespace std;


 // } Driver Code Ends
class Solution
{
    public:
    int findPlatform(int arr[], int dep[], int n)
    {
        map<int,int>mp;
        for(int i=0;i<n;i++)
        {
            mp[arr[i]]++;
        }
        for(int i=0;i<n;i++)
        {
            mp[dep[i]+1]--;
        }
        int sum=0;
        int max_val=0;
        for(auto &it:mp)
        {
            sum+=it.second;
            max_val=max(max_val,sum);
        }
        return max_val;
    }
};


