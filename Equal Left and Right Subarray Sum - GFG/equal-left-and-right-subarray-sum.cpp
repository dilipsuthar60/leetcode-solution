class Solution {
  public:
    int equalSum(int N, vector<int> &A) {
        // code here
        int right=accumulate(A.begin(),A.end(),0);
        int left=0;
        int n=A.size();
        for(int i=0;i<n;i++)
        {
            right-=A[i];
            if(left==right)
            {
                return i+1;
            }
            left+=A[i];
        }
        return -1;
    }
};


