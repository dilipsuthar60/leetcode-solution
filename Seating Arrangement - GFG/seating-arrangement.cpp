class Solution{
    public:
    bool is_possible_to_get_seats(int n, int m, vector<int>&nums)
    {
        nums.insert(nums.begin(),0);
        nums.push_back(0);
        for(int i=1;i<nums.size()-1;i++)
        {
            if(nums[i-1]+nums[i]+nums[i+1]==0)
            {
                nums[i]=1;
                n--;
            }
        }
        return n<=0;
        
    }
};

